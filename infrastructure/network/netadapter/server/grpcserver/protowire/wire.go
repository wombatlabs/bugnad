package protowire

import (
	"github.com/wombatlabs/bugnad/app/appmessage"
	"github.com/pkg/errors"
)

type converter interface {
	toAppMessage() (appmessage.Message, error)
}

// ToAppMessage converts a BugnadMessage to its appmessage.Message representation
func (x *BugnadMessage) ToAppMessage() (appmessage.Message, error) {
	if x == nil {
		return nil, errors.Wrapf(errorNil, "BugnadMessage is nil")
	}
	converter, ok := x.Payload.(converter)
	if !ok {
		return nil, errors.Errorf("received invalid message")
	}
	appMessage, err := converter.toAppMessage()
	if err != nil {
		return nil, err
	}
	return appMessage, nil
}

// FromAppMessage creates a BugnadMessage from a appmessage.Message
func FromAppMessage(message appmessage.Message) (*BugnadMessage, error) {
	payload, err := toPayload(message)
	if err != nil {
		return nil, err
	}
	return &BugnadMessage{
		Payload: payload,
	}, nil
}

func toPayload(message appmessage.Message) (isBugnadMessage_Payload, error) {
	p2pPayload, err := toP2PPayload(message)
	if err != nil {
		return nil, err
	}
	if p2pPayload != nil {
		return p2pPayload, nil
	}

	rpcPayload, err := toRPCPayload(message)
	if err != nil {
		return nil, err
	}
	if rpcPayload != nil {
		return rpcPayload, nil
	}

	return nil, errors.Errorf("unknown message type %T", message)
}

